package tasks

// Поменять местами два числа без создания временной переменной.

import "fmt"

// Замена переменных осуществляется с помощью парного переприсваивания
func Task13() {
	a := 42
	b := 21

	fmt.Printf("Значение a: %d, значение b: %d\n", a, b)

	a, b = b, a

	fmt.Printf("Значение a: %d, значение b: %d\n", a, b)
}
